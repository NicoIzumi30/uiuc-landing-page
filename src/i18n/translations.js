// Helper: safe getter with simple {{var}} interpolation
export function translate(dicts, lang, key, vars) {
  const parts = key.split('.');
  let node = dicts?.[lang];
  for (const p of parts) {
    node = node?.[p];
    if (node == null) break;
  }
  let str = typeof node === 'string' ? node : null;
  if (!str) {
    // fallback to id then en then key
    node = dicts?.id;
    for (const p of parts) {
      node = node?.[p];
      if (node == null) break;
    }
    str = typeof node === 'string' ? node : null;
  }
  if (!str) str = key;
  if (vars && typeof str === 'string') {
    return str.replace(/\{\{\s*(\w+)\s*\}\}/g, (_, k) =>
      Object.prototype.hasOwnProperty.call(vars || {}, k) ? String(vars[k]) : ''
    );
  }
  return str;
}

